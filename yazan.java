public class HalloJava {
    public static void main(String[] args) {
        // Begrüßung
        System.out.println("Hallo, willkommen in Java!");

        // Zwei Zahlen definieren
        int zahl1 = 7;
        int zahl2 = 5;

        // Addition
        int summe = zahl1 + zahl2;

        // Ergebnis ausgeben
        System.out.println("Die Summe von " + zahl1 + " und " + zahl2 + " ist: " + summe);
    }
}

